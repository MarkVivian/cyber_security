                # FOR LOOPS
for ($i=1; $i -le 5; $i++) {
    Write-Host "Iteration $i"
}
                
                # IF LOOPS
$age = 20

if ($age -ge 18) {
    Write-Host "You are an adult."
} else {
    Write-Host "You are a minor."
}
                
                # WHILE LOOPS
$count = 1

while ($count -le 5) {
    Write-Host "Iteration $count"
    $count++
}
                